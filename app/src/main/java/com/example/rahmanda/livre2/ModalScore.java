public class ModalScore {
    private int score1;
    private int score2;

    public ModalScore(){

    }

    public ModalScore(int score1, int score2) {
        this.score1 = score1;
        this.score2 = score2;
    }

    public int getScore1() {
        return score1;
    }

    public int getScore2() {
        return score2;
    }
}
