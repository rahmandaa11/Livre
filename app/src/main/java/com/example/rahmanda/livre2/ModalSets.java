public class ModalSets {
    private String set1, set2, set3, set4, set5, set6;

    public ModalSets(){

    }

    public ModalSets(String set1, String set2, String set3, String set4, String set5, String set6) {
        this.set1 = set1;
        this.set2 = set2;
        this.set3 = set3;
        this.set4 = set4;
        this.set5 = set5;
        this.set6 = set6;
    }

    public String getSet1() {
        return set1;
    }

    public String getSet2() {
        return set2;
    }

    public String getSet3() {
        return set3;
    }

    public String getSet4() {
        return set4;
    }

    public String getSet5() {
        return set5;
    }

    public String getSet6() {
        return set6;
    }
}
